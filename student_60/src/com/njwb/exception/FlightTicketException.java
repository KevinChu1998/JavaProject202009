package com.njwb.exception;

public class FlightTicketException extends Exception {

	private String errorMsg;

	/**
	 * 自定义异常构造方法，
	 * 
	 * @param errorMsg
	 *            异常信息
	 */
	public FlightTicketException(String errorMsg) {
		super();
		this.errorMsg = errorMsg;
	}

	public FlightTicketException() {
		super();
	}

	public String getErrorMsg() {
		return errorMsg;
	}
}
