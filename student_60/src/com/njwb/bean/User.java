package com.njwb.bean;

import java.util.Date;

public class User {
	/**用户名*/
	private int userId;
	/**用户名*/
	private String userName;
	/**密码*/
	private String password;
	/**身份证号*/
	private String idCardNo;
	/**真实姓名*/
	private String realName;
	/**性别*/
	private String sex;
	/**身份证号*/
	private String phone;
	/**创建时间*/
	private Date createTime;
	
	public User() {
		super();
	}

	
	public User(String userName, String password, String idCardNo, String realName, String sex, String phone) {
		super();
		this.userName = userName;
		this.password = password;
		this.idCardNo = idCardNo;
		this.realName = realName;
		this.sex = sex;
		this.phone = phone;
	}
	
	

	public User(int userId, String userName, String password, String idCardNo, String realName, String sex, String phone, Date createTime) {
		super();
		this.userId = userId;
		this.userName = userName;
		this.password = password;
		this.idCardNo = idCardNo;
		this.realName = realName;
		this.sex = sex;
		this.phone = phone;
		this.createTime = createTime;
	}


	public int getUserId() {
		return userId;
	}


	public void setUserId(int userId) {
		this.userId = userId;
	}


	public String getUserName() {
		return userName;
	}


	public void setUserName(String userName) {
		this.userName = userName;
	}


	public String getPassword() {
		return password;
	}


	public void setPassword(String password) {
		this.password = password;
	}


	public String getIdCardNo() {
		return idCardNo;
	}


	public void setIdCardNo(String idCardNo) {
		this.idCardNo = idCardNo;
	}


	public String getRealName() {
		return realName;
	}


	public void setRealName(String realName) {
		this.realName = realName;
	}


	public String getSex() {
		return sex;
	}


	public void setSex(String sex) {
		this.sex = sex;
	}


	public String getPhone() {
		return phone;
	}


	public void setPhone(String phone) {
		this.phone = phone;
	}


	public Date getCreateTime() {
		return createTime;
	}


	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}


	@Override
	public String toString() {
		return "User [createTime=" + createTime + ", idCardNo=" + idCardNo + ", password=" + password + ", phone=" + phone + ", realName=" + realName + ", sex=" + sex + ", userId=" + userId
				+ ", userName=" + userName + "]";
	}
	
	
	
	
	
	








	
	
	
}
